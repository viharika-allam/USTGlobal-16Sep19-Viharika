var fruit=new Object();
        fruit.firstName='Apple';
        fruit.lastName='Red';
        fruit.taste='good';
        fruit.getFullName=function(){
            return this.firstName+' '+this.lastName
        }
        var lastName=fruit.lastName;
        console.log('LastName',lastName);
        var fruitTaste=fruit.taste;
        console.log('Taste',fruitTaste) 
        var fullName=fruit.getFullName();
        console.log('FullName',fullName);


